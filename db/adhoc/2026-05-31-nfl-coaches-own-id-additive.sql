-- 3. Apply PFR-canonical full_name (PFR wins on mismatch; log drift), dob,
--    and pre-derived coach_id. Coaches in nfl_coaches that the fixture
--    lacks (should be zero after the Phase 0 coverage check) are left
--    with their existing full_name, NULL dob, NULL coach_id, surfacing as
--    a post-condition failure below.
DO $$
DECLARE
  drift_count int;
BEGIN
  SELECT COUNT(*) INTO drift_count
  FROM nfl_coaches c
  JOIN pfr_coach_fixture f ON f.pfr_coach_id = c.pfr_coach_id
  WHERE c.full_name IS DISTINCT FROM f.full_name;
  IF drift_count > 0 THEN
    RAISE NOTICE 'PFR full_name drift: % rows will be overwritten (PFR wins)', drift_count;
  END IF;
END $$;

