-- (a) All dim rows have non-NULL coach_id.
DO $$
DECLARE
  null_count int;
BEGIN
  SELECT COUNT(*) INTO null_count FROM nfl_coaches WHERE coach_id IS NULL;
  IF null_count > 0 THEN
    RAISE EXCEPTION 'post-condition (a) FAILED: % nfl_coaches rows have NULL coach_id', null_count;
  END IF;
  RAISE NOTICE 'post-condition (a) OK: all nfl_coaches rows have coach_id';
END $$;

-- (b) All dim coach_ids pass the format CHECK regex (constraint catches new
--     inserts; this asserts the regex matched the entire backfilled set too).
DO $$
DECLARE
  bad_count int;
BEGIN
  SELECT COUNT(*) INTO bad_count FROM nfl_coaches
   WHERE coach_id !~ '^[A-Z]{1,4}-[A-Z]{1,4}-[0-9]{4}-[0-9]{2}-[0-9]{2}(-[0-9]{2})?$';
  IF bad_count > 0 THEN
    RAISE EXCEPTION 'post-condition (b) FAILED: % nfl_coaches rows fail coach_id format', bad_count;
  END IF;
  RAISE NOTICE 'post-condition (b) OK: all coach_ids match format';
END $$;

-- (c) Zero bridge rows have any *_pfr_id IS NOT NULL paired with the
--     corresponding *_coach_id IS NULL (catches a missing dim entry from a
--     failed PFR scrape).
DO $$
DECLARE
  bad_count int;
BEGIN
  SELECT COUNT(*) INTO bad_count FROM nfl_game_coaches
   WHERE (head_coach_pfr_id      IS NOT NULL AND head_coach_id      IS NULL)
      OR (off_play_caller_pfr_id IS NOT NULL AND off_play_caller_id IS NULL)
      OR (def_play_caller_pfr_id IS NOT NULL AND def_play_caller_id IS NULL);
  IF bad_count > 0 THEN
    RAISE EXCEPTION 'post-condition (c) FAILED: % bridge rows have *_pfr_id without *_coach_id', bad_count;
  END IF;
  RAISE NOTICE 'post-condition (c) OK: all bridge *_pfr_id rows resolved to *_coach_id';
END $$;

-- (d) Exactly 47 backfill cells touched (the closed interim-coordinator
--     cells where *_pfr_id was NULL but *_coach_id is now populated).
DO $$
DECLARE
  filled_count int;
BEGIN
  SELECT
    SUM(CASE WHEN off_play_caller_pfr_id IS NULL AND off_play_caller_id IS NOT NULL THEN 1 ELSE 0 END)
    + SUM(CASE WHEN def_play_caller_pfr_id IS NULL AND def_play_caller_id IS NOT NULL THEN 1 ELSE 0 END)
    + SUM(CASE WHEN head_coach_pfr_id     IS NULL AND head_coach_id      IS NOT NULL THEN 1 ELSE 0 END)
  INTO filled_count
  FROM nfl_game_coaches;
  IF filled_count <> 47 THEN
    RAISE EXCEPTION 'post-condition (d) FAILED: backfill closed % cells, expected 47', filled_count;
  END IF;
  RAISE NOTICE 'post-condition (d) OK: backfill closed exactly 47 cells';
END $$;

