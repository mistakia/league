import { Record, List } from 'immutable'

export const Player = new Record({
  id: null,
  name: null,
  pname: null,
  fname: null,
  lname: null,
  pos: null,
  height: null,
  weight: null,
  dob: null,
  forty: null,
  bench: null,
  vertical: null,
  broad: null,
  shuttle: null,
  cone: null,
  arm: null,
  hand: null,
  dpos: null,
  college: null,
  college_division: null,
  draft_year: null,
  team: null,
  depth_position: null,
  depth_number: null,
  jersey: null,
  projections: new List()
})

export function createPlayer ({
  player,
  fname,
  lname,
  pname,
  pos1,
  height,
  weight,
  dob,
  forty,
  bench,
  vertical,
  broad,
  shuttle,
  cone,
  arm,
  hand,
  dpos,
  col,
  dv,
  start,
  cteam,
  posd,
  jnum,
  dcp,
  projections
}) {
  return new Player({
    id: player,
    name: `${fname} ${lname}`,
    pname,
    fname,
    lname,
    pos: pos1,
    height,
    weight,
    dob,
    forty,
    bench,
    vertical,
    broad,
    shuttle,
    cone,
    arm,
    hand,
    dpos,
    college: col,
    college_division: dv,
    draft_year: start,
    team: cteam,
    depth_position: posd,
    jersey: jnum,
    depth_number: dcp,
    projections: new List(projections)
  })
}
